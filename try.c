#include<stdio.h>//求源点到各点的距离
int main(void){
    int e[10][10],dis[10],book[10],i,j,m,n,t1,t2,t3,u,v,min;
    int inf=99999;
    scanf("%d %d",&n,&m);//表示顶点个数和边的条数
    for(i=1;i<=n;i++)//初始化二维数组
        for(j=1;j<=n;j++)
            if(i==j) e[i][j]=0;
            else e[i][j]=inf;
    for(i=1;i<=m;i++){
        scanf("%d%d%d",&t1,&t2,&t3);
        e[t1][t2]=t3;
    }
    for(i=1;i<=n;i++)
        dis[i]=e[1][i];//初始化dis数组，表示1号顶点到其他顶点的距离
    for(i=1;i<=n;i++)
        book[i]=0;
    book[i]=1;//记录当前已知第一个顶点的最短路径
    for(i=1;i<=n-1;i++)
        for(i=1;i<=n-1;i++){//找到离一号顶点最近的点
            min=inf;
            for(j=1;j<=n;j++){
                if(book[j]==0&&dis[j]<min){
                    min=dis[j];
                    u=j;
                }
            }
            book[u]=1;//记录当前已知离第一个顶点最近的顶点
            for(v=1;v<=n;v++){
                if(e[u][v]<inf){
                    if(dis[v]>dis[u]+e[u][v])
                        dis[v]=dis[u]+e[u][v];
                }
            }
        }
    for(i=1;i<=n;i++)
        printf("%d",dis[i]);
}
